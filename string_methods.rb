
name = 'Working with string methods'

# string length
puts name.length # 27

# string reverse
puts name.reverse # sdohtem gnirts htiw gnikroW

# string upcase
puts name.upcase # WORKING WITH STRING METHODS

# string downcase
puts name.downcase # working with string methods

# string capitalize
puts name.capitalize # Working with string methods

# string swapcase (is the opposite of capitalize)
puts name.swapcase  # wORKING WITH STRING METHODS

# string include?
puts name.include?('with') # true

# string start_with?
puts name.start_with?('Working') # true

# string end_with?
puts name.end_with?('methods') # true

# string nil?
puts name.nil? # false

